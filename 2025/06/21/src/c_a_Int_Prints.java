public class c_a_Int_Prints {
    public static void main(String[] args) {
        int num1, num2, result;
        num1 = 100;
        num2 = 50;

        result = num1 + num2;
        System.out.print("result: "+result+"\n");
        System.out.println("result: "+result+"\n");
        System.out.printf("result: %d\n", result);
    }
}
